package de.ai.rezeptverwaltung.entities;

public class Schlagwort {

	private int schlagwortId;
	private String bezeichnung;
	
	public int getSchlagwortId() {
		return schlagwortId;
	}
	public void setSchlagwortId(int schlagwortId) {
		this.schlagwortId = schlagwortId;
	}
	public String getBezeichnung() {
		return bezeichnung;
	}
	public void setBezeichnung(String bezeichnung) {
		this.bezeichnung = bezeichnung;
	}
	
}